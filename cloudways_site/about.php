  <h2 class="section-title">Background</h2>
  <p>
    I'm a multidisciplinary designer and engineer working at the intersection of computation, fabrication,
    and landscape architecture. My formal training spans three graduate programs—MIT's Design and Computation
    group, Stevens Institute's Product Architecture and Engineering program, and a Bachelor of Architecture
    from UT San Antonio—but my real education has come from two decades of hands-on work shaping both digital
    systems and physical terrain.
  </p>

  <p>
    Since 2003, I've been designing and building landscapes in the Texas Hill Country through Ingram Land
    Design Build, working alongside my father to create multi-acre lake systems, water features, and sustainable
    property restoration projects. This work demands technical precision—GPS surveying, grading calculations,
    drainage engineering—but it's fundamentally about reading topography and understanding how water moves through
    land. In parallel, I've spent years in computational design and manufacturing: developing parametric tools at
    Skidmore Owings & Merrill's BlackBox research studio, founding Flynoceros (a UAV development company), and
    currently running Daedal Industries, where I lead prototyping and fabrication for engineered products.
  </p>

  <p>
    What connects these seemingly disparate fields is a commitment to direct engagement with materials and systems.
    Whether I'm operating a bulldozer to shape a dam or programming a CNC mill to cut composite parts, the work
    requires the same discipline: careful observation, iterative problem-solving, and a willingness to make decisions
    in real time without relying on excessive documentation. I'm interested in projects that demand both conceptual
    rigor and practical execution—where ideas have to survive contact with reality.
  </p>

